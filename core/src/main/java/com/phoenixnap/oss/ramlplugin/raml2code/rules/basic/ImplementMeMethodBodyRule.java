package com.phoenixnap.oss.ramlplugin.raml2code.rules.basic;

import com.phoenixnap.oss.ramlplugin.raml2code.data.ApiActionMetadata;
import com.phoenixnap.oss.ramlplugin.raml2code.helpers.CodeModelHelper.JExtMethod;
import com.phoenixnap.oss.ramlplugin.raml2code.rules.Rule;
import com.sun.codemodel.JMethod;

/**
 * Generates an empty method body returning null and a TODO comment.
 *
 * INPUT: #%RAML 0.8 title: myapi mediaType: application/json baseUri: / /base:
 * /{id} get:
 *
 * OUTPUT: return null; //TODO Autogenerated Method Stub. Implement me please.
 *
 * The name of the field can be configured. Default is "delegate".
 *
 * @author armin.weisser
 * @since 0.4.1
 */
public class ImplementMeMethodBodyRule implements Rule<JExtMethod, JMethod, ApiActionMetadata> {

	@Override
	public JMethod apply(ApiActionMetadata endpointMetadata, JExtMethod generatableType) {
		generatableType.get().body().directStatement("return null; //TODO Autogenerated Method Stub. Implement me please.");
		return generatableType.get();
	}
}
